FROM IADQFUXU AS s
LEFT JOIN IADQFUXU AS t
  ON t.ADQPECO = 'TBBANEGA1'
 AND t.ADQFUCO = s.ADQFUCO
WHERE s.ADQPECO = 'TLORTEGA2'
  AND t.ADQFUCO IS NULL;


